package ud4.maloscheiros;


    public class CalculadoraDuplicado {
        public static int calcularAreaRectangulo(int largo, int ancho) {
            return largo * ancho;
        }

        public static int calcularAreaCuadrado(int lado) {
            return lado * lado; // los cuadrados son rectangulos y se usa el mismo metodo para calcular el Area
        }
    }
